#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	ac--;
	while (ac > 0)
	{
		while (av[ac][i])
		{
			write(1, &av[ac][i], 1);
			i++;
		}
		i = 0;
		write(1, "\n", 1);
		ac--;
	}
}
